/* use this function to obtain data of text area */
function loadData(textarea) {
    // retrieve JSON text
    var data = document.getElementById(textarea);

    // parse JSON file into array of objects
    var parse_data = JSON.parse(data.value);
    var final_data = parse_data.points;

    // create arrays to store country names, country codes and populations
    var countries = [];
    var populations = [];
    var country_code = [];

    // safe length of data object
    var len= final_data.length;

    // store country names and populations in arrays
    for (i = 0; i < final_data.length; i++)
    {
    	countries.push(final_data[i].country);
    	populations.push(final_data[i].population);
    }

    // return object containing the arrays above and the data size
    return {countries, populations, country_code, len};
}

/* use this function to get country codes for countries in data */
function getCode(data){
    // array to stores country codes in
    // store country codes in 'country_code' array, if available
    for (i = 0; i < data.len; i++)
    {
        var state = false;
        for (j = 0; j < country_codes.length; j++)
        {
            // store country code at right position in array and break loop
            if (data.countries[i] == country_codes[j][2]) 
            {
                data.country_code.push(country_codes[j][0]);
                state = true;
                break;
            }
        }
        // no country code? store null value at right position in array
        if (state == false)
        {
            data.country_code.push(null);
        }
    }

    // return 'data' object with additional array of country codes 
    return data;
}
