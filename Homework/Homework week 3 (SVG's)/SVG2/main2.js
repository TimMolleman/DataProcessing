/* on loading of screen, color the map in the svg */ 
window.onload = function() {
    // loop over country codes in data variable
    for (i = 0; i < data.len; i++)
    {
        // see if the country code is also a path in html
        var match = document.getElementById(data.country_code[i]);

        // if match is not null, call changeColor function for path in match
        if (match != null)
        {
            if (data.populations[i] < 5000000)
            {
                changeColor(match, "#fee5d9");
            }
            else if (data.populations[i] >= 5000000 && data.populations[i] < 15000000)
            {
                changeColor(match, "#fcae91");
            }
            else if (data.populations[i] >= 15000000 && data.populations[i] < 40000000)
            {
                changeColor(match, "#fb6a4a");
            }
            else if (data.populations[i] >= 40000000 && data.populations[i] < 60000000)
            {
                changeColor(match, "#de2d26");
            }
            else
            {
                changeColor(match, "#a50f15");
            }
        }
    }

    // render canvas to draw legend of the map on
    var canvas = document.getElementById("legend");
    var ctx = canvas.getContext('2d');

    // create outline for legend
    ctx.lineWidth = "0.5";
    ctx.fillStyle = "#fee5d9";
    ctx.fillRect(10,210,10,10);
    ctx.strokeRect(10,210,10,10);
    ctx.fillStyle = "#fcae91";
    ctx.fillRect(10,220,10,10);
    ctx.strokeRect(10,220,10,10);
    ctx.fillStyle = "#fb6a4a";
    ctx.fillRect(10,230,10,10);
    ctx.strokeRect(10,230,10,10);
    ctx.fillStyle = "#de2d26";
    ctx.fillRect(10,240,10,10);
    ctx.strokeRect(10,240,10,10);
    ctx.fillStyle = "#a50f15";
    ctx.fillRect(10,250,10,10);
    ctx.strokeRect(10,250,10,10);
    ctx.strokeRect(3, 203, 50, 60);

    // add text to the legend 
    ctx.fillStyle = "black";
    ctx.font = "8px";
    ctx.fillText("0-5", 25, 217.5);
    ctx.fillText("5-15", 25, 227.5);
    ctx.fillText("15-40", 25, 237.5);
    ctx.fillText("40-60", 25, 247.5);
    ctx.fillText("60+", 25, 258);
}

